package schema;

public class MonthTasks {
    private String month;
    private int count;

    public MonthTasks() {

    }

    public String getMonth() {
        return month;
    }
    public void setMonth(String month) {
        this.month = month;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
}
